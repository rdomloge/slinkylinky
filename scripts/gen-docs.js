function genFrontendComponents(components, pages) {
  let md = `# Frontend Components\n\n${GENERATED_NOTE}`;

  // ── Atoms ──
  const atoms = components.filter(c => c.file.startsWith('components/atoms/'));
  if (atoms.length) {
    md += `## Atoms\n\nSmall, reusable UI primitives in \`src/components/atoms/\`.\n\n`;
    md += `| Component | File | Props | Notes |\n|-----------|------|-------|-------|\n`;
    for (const c of atoms) {
      const props = c.props.length ? c.props.join(', ') : '—';
      md += `| \`${c.name}\` | \`${c.file}\` | ${props} | ${c.description || ''} |\n`;
    }
    md += '\n';
  }

  // ── Layout ──
  const layout = components.filter(c => c.file.startsWith('components/layout/'));
  if (layout.length) {
    md += `## Layout\n\nLayout wrappers in \`src/components/layout/\`.\n\n`;
    md += `| Component | File | Notes |\n|-----------|------|-------|\n`;
    for (const c of layout) {
      md += `| \`${c.name}\` | \`${c.file}\` | ${c.description || ''} |\n`;
    }
    md += '\n';
  }

  // ── Feature Components ──
  const feature = components.filter(
    c => !c.file.startsWith('components/atoms/') && !c.file.startsWith('components/layout/')
  );
  if (feature.length) {
    md += `## Feature Components\n\nDomain-specific components in \`src/components/\`.\n\n`;
    for (const c of feature) {
      md += `### \`${c.name}\`\n`;
      md += `**File:** \`${c.file}\`  \n`;
      if (c.description) md += `${c.description}  \n`;
      if (c.props.length) md += `**Props:** \`${c.props.join('`, `')}\`  \n`;
      if (c.importedComponents.length) md += `**Uses:** ${c.importedComponents.join(', ')}  \n`;
      if (c.apiCalls.length) md += `**API calls:** \`${c.apiCalls.join('`, `')}\`  \n`;
      md += '\n';
    }
  }

  // ── Pages ──
  md += `## Pages\n\nRoute-level page components in \`src/pages/\`.\n\n`;
  md += `| Page | File | Props/API |\n|------|------|-----------|\n`;
  for (const p of pages) {
    const notes = [
      p.props.length ? `props: ${p.props.join(', ')}` : '',
      p.apiCalls.length ? `calls: ${p.apiCalls.slice(0, 2).join(', ')}` : '',
    ].filter(Boolean).join(' | ');
    md += `| \`${p.name}\` | \`${p.file}\` | ${notes || '—'} |\n`;
  }
  md += '\n';

  return md;
}

function genFrontendPages(routes, pages) {
  let md = `# Frontend Routes & Pages\n\n${GENERATED_NOTE}`;

  md += `## Route Table\n\nDefined in \`frontend/react/src/App.jsx\`.\n\n`;
  md += `| Path | Component | Page File | Auth |\n|------|-----------|-----------|------|\n`;

  const publicPaths = new Set(['/callback', '/public/supplierresponse', '/public/leadresponse']);
  for (const r of routes) {
    const auth = publicPaths.has(r.path) ? 'Public' : 'Protected';
    md += `| \`${r.path}\` | \`${r.component}\` | \`${r.file || '?'}\` | ${auth} |\n`;
  }
  md += '\n';

  md += `## Auth / Session\n\n`;
  md += `- Auth managed by \`src/auth/AuthProvider.jsx\` (Keycloak OIDC)\n`;
  md += `- OAuth callback handled at \`/callback\` → \`src/auth/Callback.jsx\`\n`;
  md += `- All protected routes require valid JWT; use \`useAuth()\` hook from \`AuthProvider\`\n`;
  md += `- Authenticated fetch calls should use \`fetchWithAuth\` from \`@/utils/fetchWithAuth\`\n\n`;

  md += `## Page Notes\n\n`;
  for (const p of pages) {
    const route = routes.find(r => r.component === p.name || r.file?.includes(p.file.replace('pages/', '')));
    md += `### \`${p.name}\` — \`${route?.path || p.file}\`\n`;
    if (p.description) md += `${p.description}\n`;
    if (p.apiCalls.length) md += `**API:** \`${p.apiCalls.join('`, `')}\`\n`;
    if (p.props.length) md += `**Props:** ${p.props.join(', ')}\n`;
    md += '\n';
  }

  return md;
}

function genBackendEntities(entities) {
  let md = `# Backend Domain Entities\n\n${GENERATED_NOTE}`;
  md += `All JPA entities across all services.\n`;
  md += `Schema is **not** managed by Hibernate — DDL lives in \`sl-k8s-scripts/jenkins-k8s-setup/helm/*-backup.sql\`.\n\n`;

  for (const e of entities) {
    md += `## \`${e.name}\`\n`;
    md += `**Table:** \`${e.table}\`\n\n`;
    if (e.fields.length) {
      md += `**Fields:**\n`;
      for (const f of e.fields) md += `- \`${f}\`\n`;
      md += '\n';
    }
    if (e.rels.length) {
      md += `**Relationships:**\n`;
      for (const r of e.rels) md += `- ${r}\n`;
      md += '\n';
    }
  }

  return md;
}

function genBackendApi(controllers) {
  let md = `# Backend API Endpoints\n\n${GENERATED_NOTE}`;
  md += `Base path for all services: \`/.rest\` (proxied by Vite in dev, Cloudflare Tunnel in prod).\n\n`;
  md += `Spring Data REST auto-exposes CRUD at \`/.rest/{entityPluralName}\` — only *custom* controllers are listed here.\n\n`;

  for (const c of controllers) {
    if (!c.endpoints.length) continue;
    md += `## \`${c.name}\`\n`;
    md += `**Base path:** \`${c.basePath || '(Spring Data REST)'}\`  \n`;
    md += `**File:** \`${c.file}\`\n\n`;
    md += `| Method | Path | Handler |\n|--------|------|--------|\n`;
    for (const e of c.endpoints) {
      md += `| \`${e.method}\` | \`${e.fullPath || e.subPath || '/'}\` | \`${e.methodName}()\` |\n`;
    }
    md += '\n';
  }

  // Document the Spring Data REST auto-exposed repos
  md += `## Spring Data REST (auto-exposed)\n\n`;
  md += `The following resources are auto-exposed at \`/.rest/{path}\` by Spring Data REST:\n\n`;
  md += `| Resource | Base URL | Service |\n|----------|----------|---------|\n`;
  md += `| Demand | \`/.rest/demands\` | linkservice |\n`;
  md += `| DemandSite | \`/.rest/demandSites\` | linkservice |\n`;
  md += `| Supplier | \`/.rest/suppliers\` | linkservice |\n`;
  md += `| Proposal | \`/.rest/proposals\` | linkservice |\n`;
  md += `| PaidLink | \`/.rest/paidLinks\` | linkservice |\n`;
  md += `| Category | \`/.rest/categories\` | linkservice |\n`;
  md += `| BlackListedSupplier | \`/.rest/blackListedSuppliers\` | linkservice |\n`;
  md += `| Engagement | \`/.rest/engagements\` | supplierengagement |\n`;
  md += `| SupplierLead | \`/.rest/leads\` | supplierengagement |\n`;
  md += `| AuditRecord | \`/.rest/auditrecords\` | audit |\n`;
  md += `| Stats | \`/.rest/stats\` | stats |\n`;

  return md;
}

// ─────────────────────────────────────────────
// Main
// ─────────────────────────────────────────────

console.log('Generating SlinkyLinky docs index...\n');

// Frontend
const compFiles = walkDir(path.join(FRONTEND_SRC, 'components'), ['.jsx', '.js']);
const components = compFiles.map(analyzeJsxFile).filter(Boolean);

const pageFiles = walkDir(path.join(FRONTEND_SRC, 'pages'), ['.jsx']);
const pages = pageFiles.map(analyzeJsxFile).filter(Boolean);

const routes = extractRoutes();

writeDoc('frontend-components.md', genFrontendComponents(components, pages));
writeDoc('frontend-pages.md', genFrontendPages(routes, pages));

// Backend — entities across all services
const entityDirs = ['linkservice', 'supplierengagement', 'stats', 'audit']
  .map(svc => path.join(ROOT, svc, 'src', 'main', 'java'));
const javaFiles = entityDirs.flatMap(d => walkDir(d, ['.java']));
const entities = javaFiles.map(analyzeJavaEntity).filter(Boolean);
writeDoc('backend-entities.md', genBackendEntities(entities));

// Backend — controllers (linkservice + other services)
const services = ['linkservice', 'stats', 'audit', 'supplierengagement'];
const controllers = [];
for (const svc of services) {
  const svcDir = path.join(ROOT, svc, 'src', 'main', 'java');
  const files = walkDir(svcDir, ['.java']);
  controllers.push(...files.map(analyzeJavaController).filter(Boolean));
}
writeDoc('backend-api.md', genBackendApi(controllers));

// ─────────────────────────────────────────────
// Sync CLAUDE.md Quick Reference table
// ─────────────────────────────────────────────

const GENERATED_DOCS = [
  { file: 'docs/frontend-components.md', desc: 'Every React component: file path, props, API calls, sub-components used' },
  { file: 'docs/frontend-pages.md',      desc: 'Full route table (path → component → file, auth status) and per-page API calls' },
  { file: 'docs/backend-entities.md',    desc: 'All JPA entities with fields and relationships' },
  { file: 'docs/backend-api.md',         desc: 'All custom REST endpoints by controller + Spring Data REST auto-exposed resources' },
];

function buildGeneratedTable() {
  const rows = GENERATED_DOCS
    .map(({ file, desc }) => `| [\`${file}\`](${file}) | ${desc} |`)
    .join('\n');
  return `<!-- GENERATED-DOCS-TABLE:START — managed by scripts/gen-docs.js; do not edit this block manually -->\n| File | What it covers |\n|------|----------------|\n${rows}\n<!-- GENERATED-DOCS-TABLE:END -->`;
}

const claudeMdPath = path.join(ROOT, 'CLAUDE.md');
const claudeSrc = readFile(claudeMdPath);
if (claudeSrc) {
  const updated = claudeSrc.replace(
    /<!-- GENERATED-DOCS-TABLE:START[\s\S]*?GENERATED-DOCS-TABLE:END -->/,
    buildGeneratedTable()
  );
  if (updated !== claudeSrc) {
    fs.writeFileSync(claudeMdPath, updated, 'utf8');
    console.log('  updated CLAUDE.md Quick Reference table');
  }
}

console.log('\nDone. Reference these in CLAUDE.md under docs/');
