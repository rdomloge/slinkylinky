#!/usr/bin/env node
/**
 * gen-docs.js — SlinkyLinky codebase index generator
 *
 * Generates markdown reference files under docs/ to enable efficient
 * Claude Code work without repetitive scanning.
 *
 * Usage (from repo root):
 *   node scripts/gen-docs.js
 *
 * From frontend:
 *   npm run gen-docs
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const FRONTEND_SRC = path.join(ROOT, 'frontend', 'react', 'src');
const DOCS_DIR = path.join(ROOT, 'docs');
const GENERATED_NOTE = '> Auto-generated by `scripts/gen-docs.js` — run `node scripts/gen-docs.js` to refresh.\n\n';

if (!fs.existsSync(DOCS_DIR)) fs.mkdirSync(DOCS_DIR);

// ─────────────────────────────────────────────
// Utilities
// ─────────────────────────────────────────────

function readFile(p) {
  try { return fs.readFileSync(p, 'utf8'); } catch { return null; }
}

function writeDoc(name, content) {
  const p = path.join(DOCS_DIR, name);
  fs.writeFileSync(p, content, 'utf8');
  console.log(`  wrote ${path.relative(ROOT, p).replace(/\\/g, '/')}`);
}

function walkDir(dir, exts) {
  const results = [];
  if (!fs.existsSync(dir)) return results;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...walkDir(full, exts));
    } else if (exts.some(e => entry.name.endsWith(e))) {
      results.push(full);
    }
  }
  return results;
}

function rel(p) {
  return path.relative(ROOT, p).replace(/\\/g, '/');
}

function relSrc(p) {
  return path.relative(FRONTEND_SRC, p).replace(/\\/g, '/');
}

// ─────────────────────────────────────────────
// Frontend: Component Analysis
// ─────────────────────────────────────────────

function analyzeJsxFile(filePath) {
  const src = readFile(filePath);
  if (!src) return null;

  const file = relSrc(filePath);

  // Component name — prefer explicit named default export, fall back to filename
  let name =
    (src.match(/export default function (\w+)/) ||
     src.match(/^function (\w+)[^{]*\{[\s\S]*\nexport default \1/m) ||
     src.match(/const (\w+)\s*=\s*\(/) && src.includes(`export default ${src.match(/const (\w+)\s*=\s*\(/)?.[1]}`)
    )?.[1] || path.basename(filePath, path.extname(filePath));

  // Also try: export default SomeName at the bottom
  const bottomExport = src.match(/export default (\w+)\s*;?\s*$/m);
  if (bottomExport && bottomExport[1] !== 'function') name = bottomExport[1];
  // Re-check named export
  const namedDefault = src.match(/export default function (\w+)/);
  if (namedDefault) name = namedDefault[1];

  // Description — first JSDoc block or first line comment before component
  let description = '';
  const jsdoc = src.match(/\/\*\*([\s\S]*?)\*\//);
  if (jsdoc) {
    description = jsdoc[1]
      .split('\n')
      .map(l => l.replace(/^\s*\*\s?/, '').trim())
      .filter(Boolean)
      .join(' ');
  }

  // Props — from destructured function parameter
  const propsMatch = src.match(/(?:export default )?function \w*\s*\(\s*\{([^}]+)\}/);
  const props = propsMatch
    ? propsMatch[1]
        .split(',')
        .map(p => p.split('=')[0].trim().replace(/\n/g, ''))
        .filter(p => p && /^\w+$/.test(p))
    : [];

  // Local component imports from @/components or relative
  const importedComponents = [
    ...src.matchAll(/import\s+(?:\{[^}]+\}|\w+)\s+from\s+['"]@\/components\/([^'"]+)['"]/g),
  ].map(m => m[0].match(/import\s+(?:\{([^}]+)\}|(\w+))/)?.[1] || m[0].match(/import\s+(\w+)/)?.[1] || '')
   .filter(Boolean)
   .flatMap(s => s.split(',').map(x => x.trim()))
   .filter(Boolean);

  // API calls — fetch/fetchWithAuth patterns
  const apiCalls = [
    ...new Set([
      ...[...src.matchAll(/fetch(?:WithAuth)?\s*\(\s*['"`]([^'"`]+)['"`]/g)].map(m => m[1]),
      ...[...src.matchAll(/fetch(?:WithAuth)?\s*\(\s*`([^`$]+)/g)].map(m => m[1] + '…'),
    ])
  ].filter(u => u.startsWith('/') || u.startsWith('.'));

  // Whether it's a page (uses Layout) or atom
  const usesLayout = /import.*Layout/.test(src);

  return { name, file, description, props, importedComponents, apiCalls, usesLayout };
}

// ─────────────────────────────────────────────
// Frontend: Route Extraction from App.jsx
// ─────────────────────────────────────────────

function extractRoutes() {
  const src = readFile(path.join(FRONTEND_SRC, 'App.jsx'));
  if (!src) return [];
  const routes = [];
  for (const m of src.matchAll(/<Route\s+path="([^"]+)"\s+element=\{<(\w+)\s*/g)) {
    routes.push({ path: m[1], component: m[2] });
  }
  // Map component names back to page files
  const importMap = {};
  for (const m of src.matchAll(/import (\w+) from '@\/pages\/([^']+)'/g)) {
    importMap[m[1]] = `pages/${m[2]}.jsx`;
  }
  return routes.map(r => ({ ...r, file: importMap[r.component] || '' }));
}

// ─────────────────────────────────────────────
// Backend: JPA Entity Analysis
// ─────────────────────────────────────────────

function analyzeJavaEntity(filePath) {
  const src = readFile(filePath);
  if (!src || !src.includes('@Entity')) return null;

  const name = path.basename(filePath, '.java');

  const tableMatch = src.match(/@Table\s*\([^)]*name\s*=\s*"([^"]+)"/);
  const table = tableMatch ? tableMatch[1] : name.toLowerCase() + 's';

  // Primitive/simple fields: private Type field;
  const fields = [];
  for (const m of src.matchAll(/private\s+((?:String|int|long|boolean|double|float|LocalDate(?:Time)?|BigDecimal|Integer|Long|List<[^>]+>))\s+(\w+)\s*;/g)) {
    fields.push(`${m[2]}: ${m[1]}`);
  }

  // Relationships
  const rels = [];
  for (const m of src.matchAll(/@(ManyToOne|OneToMany|ManyToMany|OneToOne)[\s\S]*?private\s+\S+\s+(\w+)\s*;/g)) {
    rels.push(`${m[2]} (${m[1]})`);
  }

  return { name, table, fields, rels };
}

// ─────────────────────────────────────────────
// Backend: Controller Analysis
// ─────────────────────────────────────────────

function analyzeJavaController(filePath) {
  const src = readFile(filePath);
  if (!src) return null;
  if (!src.includes('@RestController') && !src.includes('@Controller')) return null;

  const name = path.basename(filePath, '.java');
  const basePath = src.match(/@RequestMapping\s*\(\s*(?:value\s*=\s*|path\s*=\s*)?["']([^"']+)["']/)?.[1] || '';

  // Line-by-line pass: more robust against intervening annotations
  const lines = src.split('\n');
  const endpoints = [];
  const HTTP_METHODS = ['Get', 'Post', 'Put', 'Delete', 'Patch'];

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();

    // Detect a method-level mapping annotation
    const mapMatch = line.match(/@(Get|Post|Put|Delete|Patch)Mapping/);
    if (!mapMatch) continue;

    const httpMethod = mapMatch[1].toUpperCase();

    // Extract sub-path from same line (handles multi-attribute annotations)
    // Collect full annotation (may span multiple lines if not closed yet)
    let annotationText = line;
    let j = i;
    while (!annotationText.includes(')') && j < lines.length - 1) {
      j++;
      annotationText += ' ' + lines[j].trim();
    }
    const subPathMatch = annotationText.match(/(?:path|value)\s*=\s*["']([^"']*)["']/) ||
                         annotationText.match(/@\w+Mapping\s*\(\s*["']([^"']*)["']/);
    const subPath = subPathMatch ? subPathMatch[1] : '';

    // Look forward past other annotations to find public/protected method signature
    let methodName = null;
    for (let k = i + 1; k < Math.min(i + 8, lines.length); k++) {
      const ml = lines[k].trim();
      const methodMatch = ml.match(/^(?:public|protected)\s+\S+(?:<[^>]*>)?\s+(\w+)\s*\(/);
      if (methodMatch) { methodName = methodMatch[1]; break; }
    }
    if (!methodName) continue;

    const fullPath = (basePath + (subPath ? '/' + subPath : ''))
      .replace(/\/\//g, '/').replace(/^(?!\/)/, '/');

    endpoints.push({ method: httpMethod, subPath, methodName, fullPath });
  }

  return { name, basePath, endpoints, file: rel(filePath) };
}

